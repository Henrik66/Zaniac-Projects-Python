# this turtle program draws a rectangle

import turtle

henrik = turtle.Pen()

henrik.fillcolor("blue")

henrik.begin_fill()

henrik.forward(200)

henrik.left(90)

henrik.forward(100)

henrik.left(90)

henrik.forward(200)

henrik.left(90)

henrik.forward(100)

henrik.end_fill()

henrik.penup()

henrik.forward(200)

henrik.fillcolor("orange")

henrik.pendown()

henrik.begin_fill()

henrik.left(150)

henrik.forward(150)

henrik.right(120)

henrik.forward(150)

henrik.right(120)

henrik.forward(150)

henrik.end_fill()
